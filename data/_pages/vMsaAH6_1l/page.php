<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kyotetsu-koge</b>: The Ninja&#39;s weapons are numerous, one of his favourite weapon is the&nbsp;<i>kyotetsu-koge</i>, a dagger with two blades, one curved. It is joined to a metal ring with a rope which could be wrapped around an enemy&#39;s arm or leg, to bring him down. The kyotetsu-koge basically functions as a&nbsp;<i>whip</i>&nbsp;as the Ninja attempts to entangle his enemies. It also bears two blades, one curved and one straight. The straight blade functions as a&nbsp;<i>dagger</i>&nbsp;and is used to slice the throat of any entangled opponent and the curved functions as a grappling hook.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Nage-gama</b>: This is another chain-based weapon (the chain has an&nbsp;<i>Effective</i>&nbsp;<i>Strength</i>&nbsp;of 2). It may have a kama at one end but often it might just look like a harmless walking stick. However, at one end of the pole a kusari (chain) can suddenly snap out and disorientate an opponent. This weapon is favoured by monks and the dreaded ninja. The nage-gama can entangle an opponent in the same way as a kusari-gama.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Naginata</b>: The naginata (see picture, right) consists of a very long, curved spearhead. It is said that the yamabushi invented this weapon and today its use is common. Indeed, women of the buke class are expected to master this weapon by the time they are eighteen years of age. It is known as the &quot;woman&#39;s weapon&quot; and the special arm of women of gentle blood.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Ninjato</b>: The ninjato is a straight-edged sword slightly shorter than the katana and used almost exclusively by the ninja clans. With its straight edge and shorter length it is ideal for stealthy activities.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>No-dachi</b>: This is a two-handed sword, a much larger version of the katana. It isn&#39;t an especially popular weapon, because of its length and weight, but in the hands of a skilled swordsman the no-dachi can cause terrible wounds and is quite useful against foes on horseback. The no-dachi is counted as a normal two-handed sword.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Nunchaku</b>: The nunchaku is closely related to the fighting farmers because it is connected with the cultivation of grinding and polishing rice. It consists of two blocks of wood connected by leather thongs, strands of horsehair, or even chains. It is a useful subduing weapon and it can also be used to entangle an opponent&#39;s weapon. In combat the nunchaku grants a +10 bonus to the chance to&nbsp;<i>stun</i>&nbsp;and a +10 bonus to chance of disarming an opponent. However, it requires two hands to use.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Smoke Bombs</b>: One of the favourite acts of evasion for a Ninja was the use of Smoke Bombs. Once one was used a smoke screen would hide the Ninja long enough for him to escape - thus given the appearance of sudden invisibility. In gaming terms a Smoke Bomb&#39;s effects are exactly those created by a&nbsp;<i>Mystic</i>&nbsp;<i>Mist</i>&nbsp;spell only that the diameter is 6 yards instead of 12. A Smoke Bomb is generally designed for close range use in order for the Ninja to escape from any unwanted attention. However it may be thrown a maximum of 20 yards.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Tanto</b>&nbsp;&amp;&nbsp;<b>Aikuchi</b>: Both these weapons are daggers. The former is a blade with guards and the latter is one without. Both daggers are often carried by all elements of Nipponese society as the heimin are allowed to carry them.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Tonfa</b>: This is another weapon connected with the cultivation of rice. It consists of a rectangular piece of wood, with a handle protruding on one side, near one end. Whirled around with lightning speed, this apparently innocuous tool can be used to parry or deflect a blow, and then return it with a flourish. In combat a tonfa grants a +10 bonus to the chance of stunning an opponent.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Wakizashi</b>: This is the short sword and is typically carried with a katana (daisho). It measures from sixteen to twenty inches. The wakizashi is often used to carry out ritual suicide or the beheading of an enemy but in other cases skilled bushi of kenjutsu (swordsmanship) can use both the wakizashi and katana in tandem. Although it is the case for a bushi to leave his katana, or similar weapon, in a safe place, when visiting another bushi&#39;s house or of the ruling lord&#39;s palace, the owner is allowed to keep his wakizashi as &quot;the guardian of his honour&quot;. If a warrior has Specialist Weapon &ndash; Parrying Weapon, he receives a free parry per round when using a wakizashi.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Yari</b>: Nipponese spears, or yari, are just as beautiful as their swords. The spearheads are cast of the same high quality steel used for swords and come in many shapes and lengths. The straight spearhead is the most common but there are yari with curved heads too and these are called&nbsp;<i>naginata</i>&nbsp;(classed as halberds). Some yari have barbed points, some are hooked and others are rake-like. The yari is a very popular weapon among the bushi and it is common among regiments of Ashigaru.</p>

