<h2 style="font-family: Verdana, Arial, sans-serif; font-size: 14px; color: rgb(128, 0, 0);">ON ARMS AND ARMOUR</h2>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;">Armour worn in Nippon is completely different to that worn in the Old World. It is, on the whole, lighter and is heavily leather-based. Armour is restricted to the armouries of a clan and its usage depends upon the rank of the clansman; commoners do not wear armour other than, maybe, leather. Any rank of bushi (warrior) can wear armour although the high-ranking members of the clan will inevitably wear the more elaborate pieces. Furthermore, not every warrior in a clan is armed and armoured to the teeth, as some clans are richer or poorer than others and may not even have the necessary expertise in the making of some armour and weapons.&nbsp;</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;">The warriors of a clan are equipped according to what the clan has available or what they are left by their fathers; the latter case is the most common. Generally speaking, armour and weapons are usually owned by the family and handed down from father to son (such weapons and armour have pride of place in a bushi&#39;s house where an entire room is given over to them). Trade also does happen: a warrior may buy items from the clan&#39;s artisans but he should be wary of purchasing things which are inappropriate to his rank, i.e. a low-ranking bushi is not permitted to use a longbow. A warrior might trade with another warrior if he can afford it or he might even kill him for what he wears or carries. The other way, of course, of a clan obtaining weapons and armour, is through victories in battle.</p>

<h3 style="font-family: Verdana, Arial, sans-serif; font-size: 12px; color: rgb(128, 0, 0);">NIPPONESE ARMOUR</h3>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kabuto</b>&nbsp;(Helmet). The Nipponese helmet is called a kabuto (see picture, right). They come in many varying shapes and sizes and are worn by the buke. The shapes and styles are legion with helmets resembling a demon&#39;s head, a domed mountain, a flat-topped mountain, a peach and the heads of lions, birds, and dragons. The more elaborate the kabuto the more high-ranking a bushi is. The kabuto belonging to a high-ranking bushi will have a special socket where an ornament may be placed (<i>maedate</i>, see below). A kabuto offers 1 armour point of protection to the head (+1 AP if combined with a&nbsp;<i>mempo</i>, face mask, see below).</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Mempo</b>&nbsp;(Face mask). Available to higher-ranking bushi are the face masks or&nbsp;<i>mempo</i>&nbsp;(see picture, right). They are made from a single, rigid piece of metal or leather, or from several plates hinged together to make them more flexible. Their a many different styles of mempo. Some cover the entire face with removable pieces, some cover the face below the eyes, some cover the cheeks and chin, leaving the nose and mouth exposed, others cover the lower part of the face (usually chin only) and are often referred to as &quot;swallow-face&quot;, and some cover the forehead and cheeks only. A mempo is almost always worn with a kabuto in which case it grants the wearer +1 AP to the head. If a mempo is worn on its own then it offers only 0/1 AP to the head.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Maedate</b>&nbsp;(Crest). This is frontal decoration or crest slotted into the kabuto of a high-ranking bushi to display his prowess in battle, his clan, or bravery or cunning (see the above picture). This crest doesn&#39;t offer any extra protection but may grant leadership to inspire comrades or it may even be used as a tool of intimidation.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Jingasa</b>&nbsp;(Flat helmet). Very common throughout the lower-ranking bushi (including the ashigaru) is the conical, almost flat, type of helmet. It is cast from one piece of steel, copper, iron or from as many as twenty strips of those metals riveted together in an overlapping pattern, which serves to reinforce the whole construction. They are usually decorated or embossed, with two circular patterns made of finely lacquered leather or wood. Other types of these helmets have a flexible net of mail hanging from the brim covering the wearer&#39;s shoulders and back. Some types of jingasa can be used as cooking pots. A jingasa offers 1 AP to the head and may not be combined with any other type of head armour.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kawa-gasa&nbsp;</b>(Leather helmet). Lower-ranking retainers of a clan sometimes wear this leather helmet. It is usually heavily lacquered and embossed and has a pointed crown, a broad brim coloured black, with the clan crest at the front. The kawa-gasa offers 0/1 AP of protection to the head.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kusari-gote</b>&nbsp;(Armoured sleeve) &amp;&nbsp;<b>Hansho-gote</b>&nbsp;(Leather sleeve). The armoured sleeve, or kusari-gote, is worn by many warriors. It is protected on the outside by a series of metal defences beginning with a plate covering the shoulder. Two heavy cords are usually tied to the warrior&#39;s breastplate or tied in a crisscross around the neck. The finest kusari-gotes have hands protected by embossed metal. A kusari-gote offers 1 AP to the arm (maybe combined with the&nbsp;<i>sode</i>, see below, thereby offering +1 AP bonus to the arm).&nbsp;</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;">The&nbsp;<i>hansho-gote</i>&nbsp;is a lightweight leather sleeve and is often worn by archers for ease of movement. It is worn in exactly the same way as its metal counterpart. A hansho-gote offers 0/1 AP to the arm.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Sode</b>&nbsp;(Shoulder guard). A true bushi wears a pair of sode on his arms. A sode consists of a large plate fastened to the shoulder (see picture, right). If a&nbsp;<i>bushi</i>&nbsp;wears the kusari-gote this armoured sleeved will be tied to the sode. The sode is almost always colourful with strips of metal and lacquered leather. A sode offers 1 AP of protection to the arm and when combined with an armoured sleeve it grants 2 APs protection (a sode is only really ever worn with an armoured sleeve).</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Do</b>&nbsp;(Breastplate or corselet). Most common are the breastplates that are made of several large pieces of plates laced together with strong cords. Some are made from a single large piece of metal and simulate the curvature of the body. However, these are very rare indeed. Breastplates are usually tied at the back with shoulder straps and others open at the sides giving all round protection. A&nbsp;<i>do</i>&nbsp;offers 1 AP of protection on the body.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Haramaki-do</b>. This essentially a suit of armour which incorporates the&nbsp;<i>do</i>&nbsp;(breastplate) and leg protection, which protects the knees upwards (see picture, above). The haramaki-do offers 1 AP of protection to the body and legs.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kusari-kyahan&nbsp;</b>(Armoured legging). This item of armour is worn a lot by bushi in times or peace and war. The kusari-kyahan is a mail or chain legging, which, with its length of padded mail, covers the entire leg. This grants the wearer 1 AP to the leg location. This maybe combined with the haramaki-do (granting 2 APs on legs in this case) but this is heavy and will reduce the wearer&#39;s Initiative by -10 (GMs might like to impose an additional penalty to Movement skills). No self-respecting bushi would ever want to be weighed down in this way.</p>

