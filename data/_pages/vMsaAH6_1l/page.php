<h3 style="font-family: Verdana, Arial, sans-serif; font-size: 12px; color: rgb(128, 0, 0);">NIPPONESE LEATHER ARMOUR</h3>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;">Leather is a very popular form of protection and is worn by many warriors not least because it is reasonably inexpensive. Some items of leather armour are covered with such material as sharkskin, tortoise shell, and laced whalebone, which creates beautiful patterns and lends exceptional strength to the garment (the use of Orc skin is not unheard of). Popular leather corselets include Cathayan leather, red leather, and flowered leather. Many of the corselets worn by lower-ranking bushi are made of black lacquered leather. There exists, in their own form, all the leather armour types detailed on page 122 of the WFRP rulebook with the addition of a loin guard (<i>haidate</i>) worn around the waist. This functions as leather armour and protects the legs and is usually worn in conjunction with a haramaki-do. Another popular item of leather armour is the face guard (see picture, right) and is good enough to offer 0/1 AP to the head location.</p>

<h3 style="font-family: Verdana, Arial, sans-serif; font-size: 12px; color: rgb(128, 0, 0);">NIPPONESE WEAPONS</h3>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;">In Nippon anything that can be used as a weapon will be used, from something as inconspicuous as a smoking pipe to a harmless fan. This has come about by the strict rules of Nipponese society. Commoners are not allowed to carry the established weapons, such as swords and spears, and in certain places weapons are strictly prohibited. The ever resourceful exponents of bujutsu (martial arts) have come up with many styles and techniques to use seemingly harmless objects, and the farmers themselves, with their sickles and rakes and rice-beating utensils, have had a hand in these developments. In the hands of those not schooled in these arts, a pipe or fan to be used as a weapon is about as effective as chopping down a tree with a herring. However, the weapons that are the heart and soul of Nippon are the longbow, the spear (or yari) and, of course, the sword or katana. Only the warrior class of Nippon are legally permitted to carry such weapons. Should a commoner, or heimin, be found in possession of these weapons he will be in danger of losing his life.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Daito</b>: This sword is longer than a katana but shorter than a no-dachi (two-handed sword). This weapon is often carried by officers in battle and can be used one or two-handed in combat. The daito is classed as a hand-and-a-half sword (Bastard Sword) for WFRP purposes.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Fan</b>: Incredibly something as harmless-looking as a fan can be used as a weapon, albeit one with iron edges. The art of the war fan is known as&nbsp;<i>tessenjutsu</i>&nbsp;and masters of it can use a fan for defence and attack and could also throw it. The fan in Nipponese society has always been a symbol of prestige. In the hands of one not schooled in tessenjutsu, a fan is nothing but a useless artifact. A master of tessenjutsu may throw a War Fan like a throwing knife.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Jitte</b>: Essentially a parrying weapon, the jitte can also be used to break weapons but it is equally effective as a thrusting weapon especially to the eyes and throat. It is best described as a forked dirk and is used widely by Nipponese watchmen (doshin). A successful parry with a jitte will break a sword or dagger on a successful Strength test. Skilled users of the jitte, one with jittejutsu, can even throw this weapon.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Himogatana</b>: This is a one piece stiletto made of fine steel. It is an ideal weapon when used by assassins as it can be easily concealed. However, when used without the element of surprise the himogatana is relatively ineffective in hand-to-hand combat.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kaiken</b>: This is a dagger primarily used by women. Women of the buke class always carry a kaiken and some can wield it with exceptional skill and throw it with deadly accuracy. But not only is this weapon used for attack or defence it is also used by the woman to commit suicide, should it be necessary to do so.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kama</b>: This is the humble farmer&#39;s sickle. It is used a lot by farmers to defend themselves. The kama is also used by warriors when a length of chain is attached to it and this is called the kusari-gama (see below).</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Katana</b>: This sword can be seen as the symbol of the&nbsp;<i>buke</i>. Only the buke are legally allowed to carry the katana and many of its bushi (warriors) carry the daisho, which is the katana and wakizashi (see below) combined and typically carried in a sash tied round the waist. This weapon, like so many Nipponese blades, is an item of beauty and extremely sharp. The katana is counted as a normal hand weapon for WFRP gaming purposes.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;"><b>Kusari-gama</b>: This weapon is used by warriors in order to reduce the effectiveness of the terrifying sword and the strategic use of the yari. It is basically a sickle with a length of chain, made of iron or steel, attached to its handle. This weapon may be used to entangle an opponent if a character attacks with the chain (kusari), which has an&nbsp;<i>Effective Strength</i>&nbsp;of 2. The character must pass a BS roll (hit location determined normally) to hit instead of WS.</p>

<p style="color: rgb(0, 0, 0); font-family: Verdana, Arial, sans-serif; font-size: 12px; line-height: normal;">This hit location table should be used for any weapons that can entangle an opponent (usually the ones with chains). Any hit from such a weapon, whether it causes damage or not, may entangle the enemy unless he makes a successful&nbsp;<b>Dex</b>&nbsp;test with a -10 penalty, which must be made each round. An entangled creature fights with a -10 penalty to relevant skills, which may be increased to -20 if the user does nothing other than continue to pull hard on the chain handle.</p>

